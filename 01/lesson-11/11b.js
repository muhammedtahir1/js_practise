// Multiples of 2
for (let i = 0; i <= 10; i += 2) {
  // console.log(i);
}

let j = 0;
while (j <= 10) {
  // console.log(j);
  j++;
}

// from 5 to 0
for (let i = 5; i >= 0; i--) {
  // console.log(i);
}

let k = 5;
while (k >= 0) {
  // console.log(k);
  k--;
}

